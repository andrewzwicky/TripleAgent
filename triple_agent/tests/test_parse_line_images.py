@pytest.mark.parsing
@pytest.mark.parametrize("image_name, expecteds", LINE_IMAGE_TEST_CASES)
def test_parse_line_images(
    image_name: str,
    expecteds: Tuple[
        str,
        str,
        str,
        Tuple[Characters],
        float,
        Tuple[Roles],
        Tuple[Books],
        TimelineCategory,
        Missions,
        ActionTest,
    ],
):
    line_image = cv2.imread(
        os.path.join(TEST_FOLDER, "test_line_images", f"{image_name}.png")
    )
    event = process_line_image(line_image)

    (
        e_actor,
        e_raw_time_str,
        e_event,
        e_cast_name,
        e_time_in_sec,
        e_role,
        e_books,
        e_category,
        e_mission,
        e_action_test,
    ) = expecteds

    assert event.actor == e_actor
    assert event._raw_time_str == e_raw_time_str
    assert event.event == e_event
    assert event.cast_name == e_cast_name
    assert event.time == e_time_in_sec
    assert event.role == e_role
    assert event.books == e_books
    assert event.category == e_category
    assert event.mission == e_mission
    assert event.action_test == e_action_test


